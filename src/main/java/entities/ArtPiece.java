package entities;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "obra")
public class ArtPiece implements Serializable{
 
    private Integer id  ;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Integer getCost() {
        return cost;
    }

    public void setCost(Integer cost) {
        this.cost = cost;
    }

    public Integer getExposition_id() {
        return exposition_id;
    }

    public void setExposition_id(Integer exposition_id) {
        this.exposition_id = exposition_id;
    }
    private String name;
    private String type;
    private Integer cost;
    private Integer exposition_id;
}
